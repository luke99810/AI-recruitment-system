AMBIGUITY_FOLLOWUP_PROMPT = """请针对以下简历中的模糊点，生成深度追问问题。

【候选人简历】
{resume_json}

【已识别的模糊点】
{ambiguous_points}

请为每个模糊点生成 3-5 个递进式追问问题，模拟真实面试官的追问风格。
追问应该由浅入深，从表面信息逐步深入到技术细节和底层原理。

请严格按照以下 JSON 格式输出（不要输出任何其他内容）：
{{
  "followup_groups": [
    {{
      "ambiguous_point": "对应的模糊点描述",
      "severity": "high/medium/low",
      "followups": [
        {{
          "order": 1,
          "question": "追问问题文本",
          "intent": "这个追问想暴露什么问题",
          "expected_answer": "期望候选人能回答到的程度",
          "red_flag": "如果候选人这样回答，说明存在风险（危险信号）"
        }}
      ]
    }}
  ]
}}

追问设计原则：
1. 第1问：直接询问模糊点的具体情况（如「你提到用了XX技术，具体是怎么用的？」）
2. 第2问：追问技术细节或实现方案（如「为什么选择这个方案而不是另一个？」）
3. 第3问：追问边界条件或扩展性（如「如果数据量翻10倍，你的方案还能支撑吗？」）
4. 第4-5问：追问底层原理或反思（如「如果重新做，你会怎么改进？」）
5. red_flag 要具体，如「只会说概念，举不出实际例子」"""

